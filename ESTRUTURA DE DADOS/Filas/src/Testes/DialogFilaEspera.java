    private void buttonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAddActionPerformed
        // TODO add your handling code here:
        if (textNome.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Obrigatório");
            textNome.requestFocus();
            return;
        }
        areaTexto.setText(
        fila.enQueue(textNome.getText().toUpperCase())?"Adicionado":
                "OVERFLOW");
        textNome.setText("");
        textNome.requestFocus();
        //
        try{
            textTamanho.setText(this.retornaTamanho()+"");
        }catch(CloneNotSupportedException ex){
            System.out.println("ERRO");
        }
    }//GEN-LAST:event_buttonAddActionPerformed

    private void buttonAtenderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAtenderActionPerformed
        // TODO add your handling code here:
        if (fila.isEmptyQueue()){
            areaTexto.setText("FILA VAZIA");
            return;
        }
        areaTexto.setText(fila.firstQueue() + " será atendido");
        fila.deQueue();
        try{
            textTamanho.setText(retornaTamanho()+"");
        }catch(CloneNotSupportedException ex){
            System.out.println("ERRO:"+ex.getMessage());
        }
    }//GEN-LAST:event_buttonAtenderActionPerformed

