    private int retornaTamanho() throws CloneNotSupportedException{
        Fila temp = fila.clone();
        int tamanho=0;
        while(!temp.isEmptyQueue()){
            tamanho++;
            temp.deQueue();
        }
        return tamanho;
    }
    
