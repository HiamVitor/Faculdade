    private void iniciaComponentes(){
        textId.setText("");
        textNome.setText("");
        textValor.setText("");
        textNome.requestFocus();
    }
    //cria um objeto com as informações da tela
    private Categoria createObject(){
        return new Categoria(
                textId.getText().isEmpty()?0:Integer.parseInt(textId.getText()), 
                textNome.getText(), 
                Double.parseDouble(textValor.getText()));
    }
    //preenche os componentes com os valores de objeto
    private void populateComponents(Categoria categoria){
        textId.setText(categoria.getId()+"");
        textNome.setText(categoria.getNome());
        textValor.setText(categoria.getValor()+"");
    }
    //carregar a list com os dados
    private void loadList(){
        DefaultListModel lm = new DefaultListModel();
        lm.addAll(dao.getList());
        listDados.setModel(lm);
    }
    //carregar a lista com dados de filtro
    private void loadList(String filtro){
        DefaultListModel lm = new DefaultListModel();
        lm.addAll(dao.getList(filtro));
        listDados.setModel(lm);
    }

