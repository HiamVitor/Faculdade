    private void textIdCursoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_textIdCursoFocusLost
        // TODO add your handling code here:
        if (textIdCurso.getText().isEmpty()){
            return;
        }
        try{
            Integer.parseInt(textIdCurso.getText());
            String SQL="SELECT NOME FROM CURSO WHERE ID="
                    +textIdCurso.getText();
            ResultSet rs = Banco.getStatement().executeQuery(SQL);
            if (rs.next()){
                textNomeCurso.setText(rs.getString("NOME"));
            }else{
                JOptionPane.showMessageDialog(null, "Não Localizado");
                textIdCurso.requestFocus();
                return;
            }
        }catch(SQLException ex){
            System.out.println("ERRO:"+ex.getMessage());
        }catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(null, "Inválido");
            textIdCurso.requestFocus();
        }
    }//GEN-LAST:event_textIdCursoFocusLost

