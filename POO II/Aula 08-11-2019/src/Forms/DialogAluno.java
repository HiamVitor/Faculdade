    private int add() throws SQLException, ParseException{
        String SQL="INSERT INTO ALUNO (NOME,CPF,NASCIMENTO,ID_CURSO) "
                + " VALUES (?,?,?,?)";
        PreparedStatement pst = 
                Banco.getConnection().prepareStatement(SQL);
        pst.setString(1, textNome.getText().toUpperCase());
        pst.setString(2, textCPF.getText());
        Date data = formato.parse(textNascimento.getText());
        pst.setDate(3, new java.sql.Date(data.getTime()));
        pst.setInt(4, Integer.parseInt(textIdCurso.getText()));
        return pst.executeUpdate();        
    }

