    private void buttonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAddActionPerformed
        // TODO add your handling code here:
        if (textNome.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Obrigatório");
            textNome.requestFocus();
            return;
        }
        if (textCnpj.getText().equals("  .   .   /    -  ")){
            JOptionPane.showMessageDialog(null, "Obrigatório");
            textCnpj.requestFocus();
            return;
        }        
        if (textEndereco.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Obrigatório");
            textEndereco.requestFocus();
            return;
        }
        if (textTelefone.getText().equals("(  )     -    ")){
            JOptionPane.showMessageDialog(null, "Obrigatório");
            textCnpj.requestFocus();
            return;
        }  
        if (Listas.getCidadeList().isEmpty()){
            JOptionPane.showMessageDialog(null, "Cadastre uma Cidade");
            return;
        }
        Fornecedor fornecedor = new Fornecedor(
                textNome.getText(), 
                textCnpj.getText(), 
                textEndereco.getText(), 
                textTelefone.getText(), 
                (Cidade)comboCidade.getSelectedItem());
        Listas.getFornecedorList().add(fornecedor);
        textNome.setText("");
        textCnpj.setText("");
        textEndereco.setText("");
        textTelefone.setText("");
        comboCidade.setSelectedIndex(0);
        //
        textNome.requestFocus();
    }//GEN-LAST:event_buttonAddActionPerformed

    private void buttonMostraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonMostraActionPerformed
        // TODO add your handling code here:
        if (Listas.getFornecedorList().isEmpty()){
            areaTexto.setText("LISTA VAZIA");
            return;
        }
        areaTexto.setText("NOME\tCNPJ\tENDERECO\tTELEFONE\tCIDADE\n");
        for(Fornecedor fornecedor : Listas.getFornecedorList()){
            areaTexto.append(fornecedor.getNome()+"\t"+
                             fornecedor.getCnpj()+ "\t"+
                             fornecedor.getEndereco()+"\t"+
                             fornecedor.getTelefone()+"\t"+
                             fornecedor.getCidade() + "\t" +
                             fornecedor.getCidade().getSigla().name()+"\n");
        }
    }//GEN-LAST:event_buttonMostraActionPerformed

    private void buttonMostraCidadeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonMostraCidadeActionPerformed
        // TODO add your handling code here:
        Cidade cidade = (Cidade)comboCidade.getSelectedItem();
        areaTexto.setText("Fornecedores de: "+cidade.getNome()+"\n");
        for(Fornecedor fornecedor : Listas.getFornecedorList()){
            if (fornecedor.getCidade().equals(cidade)){
                areaTexto.append(fornecedor.getNome() + "\t"+
                        fornecedor.getCidade()+"\n");
            }
        }
    }//GEN-LAST:event_buttonMostraCidadeActionPerformed

