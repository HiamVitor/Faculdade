    private void btAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAdicionarActionPerformed
        // TODO add your handling code here:
        Produtos produto = new Produtos(
                textNome.getText(), 
                (Unidades)cbUnidade.getSelectedItem(), 
                Double.parseDouble(textPreco.getText()),
                (Cliente)comboCliente.getSelectedItem());
        //
        Listas.getListaProduto().add(produto);
        //
        textNome.setText("");
        textPreco.setText("");
        cbUnidade.setSelectedIndex(0);
    }//GEN-LAST:event_btAdicionarActionPerformed

    private void btMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btMostrarActionPerformed
        // TODO add your handling code here:
         areaTexto.setText("Modelo\tValor\tUnidade\tCliente\n");
        Listas.getListaProduto().forEach((produto)->
        {
            areaTexto.append(produto.getNome() +"\t"+
                    produto.getPreco() + "\t" +
                    produto.getUnidade()+"\t" + 
                    Abstratas.Listas.getListaCliente()+"\n");
        }
        
        );
    }//GEN-LAST:event_btMostrarActionPerformed

