    private void buttonAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAdicionarActionPerformed
        // TODO add your handling code here:
        if (textModelo.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Obrigatório");
            textModelo.requestFocus();
            return;
        }
        LocalDate data = null;
        try{
            data = LocalDate.parse(textAquisicao.getText(), formato);
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, "Data Inválida");
            textAquisicao.requestFocus();
        }
        Dados.getListaEquipamento().add(
        new Equipamento(
                textModelo.getText(), 
                (Tipo)comboTipo.getSelectedItem(), 
                data, 
                (Cliente)comboCliente.getSelectedItem())
        );
        textModelo.setText("");
        comboTipo.setSelectedIndex(0);
        textAquisicao.setText("");
        comboCliente.setSelectedIndex(0);
        textModelo.requestFocus();
    }//GEN-LAST:event_buttonAdicionarActionPerformed

    private void buttonMostrarTodosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonMostrarTodosActionPerformed
        // TODO add your handling code here:
        areaTexto.setText("Modelo\tAquisição\tCliente\n");
        Dados.getListaEquipamento().forEach((equipamento)->
        {
            areaTexto.append(equipamento.getModelo()+"\t"+
                    equipamento.getAquisicao().format(formato)+"\t"+
                    equipamento.getCliente()+"\n");
        }
        
        );
    }//GEN-LAST:event_buttonMostrarTodosActionPerformed

