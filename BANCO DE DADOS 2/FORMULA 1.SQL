CREATE TABLE PAISES(
SIGLA CHAR(3) NOT NULL,
NOME  VARCHAR(70) NOT NULL,
CONSTRAINT PK_PAISES PRIMARY KEY(SIGLA)
);

CREATE TABLE EQUIPES(
I_EQUIPE INTEGER NOT NULL,
NOME     VARCHAR(70) NOT NULL,
SIGLA    CHAR(3) NOT NULL,
CONSTRAINT PK_EQUIPES PRIMARY KEY(I_EQUIPE),
CONSTRAINT FK_EQUIPES_PAISES FOREIGN KEY(SIGLA)
REFERENCES PAISES(SIGLA)
);

CREATE TABLE CIRCUITOS(
I_CIRCUITO INTEGER NOT NULL,
NOME       VARCHAR(70),
SIGLA    CHAR(3) NOT NULL,
CONSTRAINT PK_CIRCUITOS PRIMARY KEY(I_CIRCUITO),
CONSTRAINT FK_CIRCUITOS_PAISES FOREIGN KEY(SIGLA)
REFERENCES PAISES(SIGLA)
);

CREATE TABLE PILOTOS(
I_PILOTO INTEGER NOT NULL,
NOME     VARCHAR(70) NOT NULL,
I_EQUIPE INTEGER,
SIGLA    CHAR(3) NOT NULL,
CONSTRAINT PK_PILOTOS PRIMARY KEY(I_PILOTO),
CONSTRAINT FK_PILOTOS_EQUIPES FOREIGN KEY(I_EQUIPE)
REFERENCES EQUIPES(I_EQUIPE),
CONSTRAINT FK_PILOTOS_PAISES FOREIGN KEY(SIGLA)
REFERENCES PAISES(SIGLA)
);

CREATE TABLE CORRIDAS(
I_CIRCUITO INTEGER NOT NULL,
DATA     DATE    NOT NULL,
DURACAO    INTEGER,
CONSTRAINT PK_CORRIDAS PRIMARY KEY(DATA),
CONSTRAINT FK_CORRIDAS_CIRCUITOS FOREIGN KEY(I_CIRCUITO)
REFERENCES CIRCUITOS(I_CIRCUITO)
);

CREATE TABLE POSICOES(
I_PILOTO INTEGER NOT NULL,
DATA     DATE    NOT NULL,
POSICAO  INTEGER NOT NULL,
CONSTRAINT PK_POSICOES PRIMARY KEY(I_PILOTO, DATA),
CONSTRAINT FK_POSICOES_PILOTOS FOREIGN KEY(I_PILOTO)
REFERENCES PILOTOS(I_PILOTO),
CONSTRAINT FK_POSICOES_CORRIDAS FOREIGN KEY(DATA)
REFERENCES CORRIDAS(DATA)
);




