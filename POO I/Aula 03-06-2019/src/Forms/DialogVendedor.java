    private void ButtonVendedorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonVendedorActionPerformed
        // TODO add your handling code here:
        Vendedor vendedor = new Vendedor(textNome.getText(), texteTelefone.getText(),Double.parseDouble(textComicao.getText()));
        Estaticos.Dados.getListaVendedor().add(vendedor);
        //
        textNome.setText("");
        textComicao.setText("");
        texteTelefone.setText("");
        textNome.requestFocus();
    }//GEN-LAST:event_ButtonVendedorActionPerformed

    private void ButtonMostrarVendedorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonMostrarVendedorActionPerformed
        // TODO add your handling code here:
        areaTexto.setText("====Vendedores====\n");
        for (Vendedor vendedor : Estaticos.Dados.getListaVendedor()){
            areaTexto.append(vendedor.getNome()+"\t"+ vendedor.getTelefone()+"\t"+ vendedor.getComissao()+"\n");
        }
    }//GEN-LAST:event_ButtonMostrarVendedorActionPerformed

