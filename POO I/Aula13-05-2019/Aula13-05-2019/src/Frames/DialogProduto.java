    private void buttonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAddActionPerformed
        // TODO add your handling code here:
        Produto produto = new Produto(textNome.getText(),
                (String)comboUnidade.getSelectedItem(),
                Double.parseDouble(textCusto.getText()),
                Double.parseDouble(textVenda.getText())
        );
        lista.add(produto);
        //
        textNome.setText("");
        comboUnidade.setSelectedIndex(0);
        textCusto.setText("");
        textVenda.setText("");
        textNome.requestFocus();
        
    }//GEN-LAST:event_buttonAddActionPerformed

    private void ButtonMostraProdutosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonMostraProdutosActionPerformed
        // TODO add your handling code here:
        if(lista.isEmpty()){
            areaTexto.setText("Lista VAZIA");
            return;
        }
        areaTexto.setText("Nome\tUnidade\tCusto\tVenda\n");
        for (Produto produto : lista) {
            areaTexto.append(produto.getNome() +"\t" 
                    +produto.getUnidade() +"\t"
                    +produto.getCusto() +"\t"
                    +produto.getVenda()
                    +"\n");
            
        }
    }//GEN-LAST:event_ButtonMostraProdutosActionPerformed

