    private void ButtonCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonCalcularActionPerformed
        // TODO add your handling code here:        
        try {
        Consumo consumo = new Consumo(textNome.getText(), Double.parseDouble(textKm.getText()),Double.parseDouble(texteLitros.getText()));
        Listas.getListaConsumo().add(consumo);            
        } catch (NumberFormatException ex){
            JOptionPane.showMessageDialog(null, "ERRO");
        }
            
        textNome.setText("");
        textKm.setText("");
        texteLitros.setText("");        
        textNome.requestFocus();
    }//GEN-LAST:event_ButtonCalcularActionPerformed

    private void ButtonMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonMostrarActionPerformed
        // TODO add your handling code here:
        
        areaTexto.setText("Modelo do Carro:" + "\t" + "Km Feitos:" + "\t" + "Litros Abastrcidos:" + "\t" + "Km/L:" + "\t" 
                + "Valor Abastecimento: " +"\n");
        for (Consumo consumo : Listas.getListaConsumo()){
            areaTexto.append(consumo.getNome()+ "\t"+ "\t" + consumo.getKm()+ "\t"+ "\t" + consumo.getLitros() + "\t" 
                    + consumo.getKm()/consumo.getLitros() + "\t" + consumo.getLitros()* Listas.getValorLitro() + "\n");
        }
    }//GEN-LAST:event_ButtonMostrarActionPerformed

