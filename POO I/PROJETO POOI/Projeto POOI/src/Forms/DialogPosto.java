    private void ButtonCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonCadastrarActionPerformed
        // TODO add your handling code here:
        try {
        Posto postos = new Posto (textNome.getText(), textNomePosto.getText(), textEndereco.getText());
        Listas.getListaPosto().add(postos);            
        } catch (NumberFormatException ex){
            JOptionPane.showMessageDialog(null, "ERRO");
        }
        textNome.setText("");
        textNomePosto.setText("");
        textEndereco.setText("");
        textNome.requestFocus();
    }//GEN-LAST:event_ButtonCadastrarActionPerformed

    private void buttonMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonMostrarActionPerformed
        // TODO add your handling code here:
        AreaTexto.setText("Nome do Frentista:" + "\t" + "Nome do Posto:" + "\t" + "Endereço:" + "\n");
        for (Posto posto : Listas.getListaPosto()){
            AreaTexto.append(posto.getNome()+ "\t" + "\t" + posto.getLocal()+ "\t" + "\t" + posto.getEndereco() + "\n");
        }
    }//GEN-LAST:event_buttonMostrarActionPerformed

